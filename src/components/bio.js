import React from "react"
import { useStaticQuery, graphql } from "gatsby"
import Image from "gatsby-image"

import { rhythm } from "../utils/typography"

const Bio = () => {
  const data = useStaticQuery(graphql`
    query BioQuery {
      avatar: file(absolutePath: { regex: "/leon.png/" }) {
        childImageSharp {
          fixed(width: 50, height: 50) {
            ...GatsbyImageSharpFixed
          }
        }
      }
      site {
        siteMetadata {
          author {
            name
            summary
          }
          social {
            twitter {
              url
              name
            }
            github {
              url
              name
            }
            portfolio {
              url
              name
            }
          }
        }
      }
    }
  `)

  const { author, social } = data.site.siteMetadata
  return (
    <div
      style={{
        display: `flex`,
        marginBottom: rhythm(2.5),
      }}
    >
      <Image
        fixed={data.avatar.childImageSharp.fixed}
        alt={author.name}
        style={{
          marginRight: rhythm(1 / 2),
          marginBottom: 0,
          minWidth: 50,
          borderRadius: `100%`,
        }}
        imgStyle={{
          borderRadius: `50%`,
        }}
      />
      <p>
        Written by <strong>{author.name}</strong>. {author.summary}
        {` `}
        Follow me on &nbsp;
        <a href={social.twitter.url} target="_blank" rel="noreferrer">
          Twitter
        </a>
        &nbsp;and check out my&nbsp;
        <a href={social.github.url} target="_blank" rel="noreferrer">
          Github
        </a>
        &nbsp; profile as well as my &nbsp;
        <a href={social.portfolio.url} target="_blank" rel="noreferrer">
          portfolio
        </a>
        &nbsp;site :D
      </p>
    </div>
  )
}

export default Bio
